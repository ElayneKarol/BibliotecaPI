package ifrn.pi.bibli.models;

public class Bibli {

	private String titulo;
	private String autor;
	private String editora;
	private String dataPubli;
	private String dataEdi;

	public String getTitulo() {
		return titulo;
	}

	public void setTitulo(String titulo) {
		this.titulo = titulo;
	}

	public String getAutor() {
		return autor;
	}

	public void setAutor(String autor) {
		this.autor = autor;
	}

	public String getEditora() {
		return editora;
	}

	public void setEditora(String editora) {
		this.editora = editora;
	}

	public String getDataPubli() {
		return dataPubli;
	}

	public void setDataPubli(String dataPubli) {
		this.dataPubli = dataPubli;
	}

	public String getDataEdi() {
		return dataEdi;
	}

	public void setDataEdi(String dataEdi) {
		this.dataEdi = dataEdi;
	}

}
